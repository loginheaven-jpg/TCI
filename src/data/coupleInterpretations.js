export const getCoupleLevel = (percentile) => {
  if (percentile >= 67) return 'High';
  if (percentile >= 34) return 'Medium';
  return 'Low';
};

export const getGapCategory = (scoreA, scoreB) => {
  const gap = Math.abs(scoreA - scoreB);
  if (gap <= 10) return 'similar';
  if (gap <= 25) return 'moderate';
  return 'contrast';
};

export const getCombinationKey = (levelA, levelB) => {
  const shortA = levelA === 'High' ? 'H' : levelA === 'Medium' ? 'M' : 'L';
  const shortB = levelB === 'High' ? 'H' : levelB === 'Medium' ? 'M' : 'L';
  return `${shortA}-${shortB}`;
};

export const getGapLabel = (category) => {
  if (category === 'similar') return '유사';
  if (category === 'moderate') return '보통';
  return '대비';
};

export const getGapColor = (category) => {
  if (category === 'similar') return { bg: 'bg-green-100', text: 'text-green-700', dot: '#10B981' };
  if (category === 'moderate') return { bg: 'bg-yellow-100', text: 'text-yellow-700', dot: '#F59E0B' };
  return { bg: 'bg-red-100', text: 'text-red-700', dot: '#EF4444' };
};
