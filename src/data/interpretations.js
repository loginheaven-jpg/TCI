/**
 * T점수를 L/M/H 레벨로 변환
 * @param {number} tScore - T점수
 * @returns {string} 'L', 'M', 또는 'H'
 */
export const getTScoreLevel = (tScore) => {
  if (tScore < 45) return 'L';
  if (tScore <= 55) return 'M';
  return 'H';
};

/**
 * 백분위를 L/M/H 레벨로 변환
 * @param {number} percentile - 백분위
 * @returns {string} 'L', 'M', 또는 'H'
 */
export const getPercentileLevel = (percentile) => {
  if (percentile < 30) return 'L';
  if (percentile <= 70) return 'M';
  return 'H';
};

/**
 * 기질 유형 코드 계산
 * @param {object} member - 참가자 데이터 (ns_t, ha_t, rd_t 포함)
 * @returns {string} 기질 유형 코드 (예: 'HML')
 */
export const calculateTemperamentType = (member) => {
  const ns = getTScoreLevel(member.ns_t || member.NST);
  const ha = getTScoreLevel(member.ha_t || member.HAT);
  const rd = getTScoreLevel(member.rd_t || member.RDT);
  return `${ns}${ha}${rd}`;
};

/**
 * 성격 유형 코드 계산
 * @param {object} member - 참가자 데이터 (sd_t, co_t, st_t 포함)
 * @returns {string} 성격 유형 코드 (예: 'HHL')
 */
export const calculateCharacterType = (member) => {
  const sd = getTScoreLevel(member.sd_t || member.SDT);
  const co = getTScoreLevel(member.co_t || member.COT);
  const st = getTScoreLevel(member.st_t || member.STT);
  return `${sd}${co}${st}`;
};

/**
 * 성격장애 경향성 체크
 * @param {number} sd_p - 자율성 백분위
 * @param {number} co_p - 연대감 백분위
 * @returns {object} { warning: boolean, message: string|null }
 */
export const checkPersonalityDisorderTendency = (sd_p, co_p) => {
  if ((sd_p < 30 && co_p < 30) || (sd_p + co_p) < 30) {
    return {
      warning: true,
      severity: 'high',
      message: '자율성(SD)과 연대감(CO)의 백분위 점수가 모두 30점 미만이거나 합산 점수가 30점 미만입니다. 적응상의 어려움을 보이고 미성숙하며 성격장애를 보일 가능성이 높습니다. 전문적인 상담이 권장됩니다.'
    };
  }
  
  if (sd_p < 30 || co_p < 30) {
    return {
      warning: true,
      severity: 'moderate',
      message: `${sd_p < 30 ? '자율성(SD)' : '연대감(CO)'}의 백분위 점수가 30점 미만입니다. 해당 영역의 발달에 관심이 필요합니다.`
    };
  }
  
  return { warning: false, severity: 'none', message: null };
};

